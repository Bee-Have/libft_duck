#include <unistd.h>
#include <stdio.h>
#include <stdlib.h>
#include "libft.h"

void	ft_lstdelone(t_list *lst, void (*del)(void *))
{
	if (!lst)
		return ;
	if (lst->content)
	{
		del(lst->content);
		lst->content = NULL;
	}
	free(lst);
	lst = NULL;
}
