#include "libft.h"

void	ft_lstdelone(t_list *lst, void (*del)(long long int *))
{
	if (!lst)
		return ;
	if (lst->value)
	{
		del(&lst->value);
		lst->value = 0;
	}
	free(lst);
	lst = NULL;
}
