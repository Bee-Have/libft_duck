#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned int		i;
	char				*tab;

	i = 0;
	tab = (char *)s;
	if (n == 0)
		return ;
	while (i < n)
	{
		tab[i] = '\0';
		i++;
	}
}
